package kpu.fs.alpha;

public class Main_Course {

    private String CourseID;
    private String CourseName;
    private String YearToBeTaken;
    private String Program;
    private String ComputersNeeded;

    public String getCourseID() {
        return CourseID;
    }
    public void setCourseID(String CourseID) {
        this.CourseID = CourseID;
    }
    public String getCourseName() {
        return CourseName;
    }
    public void setCourseName(String CourseName) {
        this.CourseName = CourseName;
    }
    public String getYearToBeTaken() {
        return YearToBeTaken;
    }
    public void setYearToBeTaken(String YearToBeTaken) {
        this.YearToBeTaken = YearToBeTaken;
    }
    public String getProgram() {
        return Program;
    }
    public void setProgram(String Program) {
        this.Program = Program;
    }
    public String getComputersNeeded() {
        return ComputersNeeded;
    }
    public void setComputersNeeded(String ComputersNeeded) {
        this.ComputersNeeded = ComputersNeeded;
    }
}

